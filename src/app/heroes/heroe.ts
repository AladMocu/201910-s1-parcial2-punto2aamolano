export class Heroe {

    id:number;
    name:string;
    image:string;
    power:number;
    wisdom:number;


    constructor(id: number, name: string, image: string, power: number, wisdom: number) {
        this.id = id;
        this.name = name;
        this.image = image;
        this.power = power;
        this.wisdom = wisdom;
    }


}